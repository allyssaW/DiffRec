def UniformSample(dataset: GraphData):
    dataset : GraphData
    sample_size = dataset.trainDataSize
    users = np.random.randint(0, dataset.n_user, sample_size)
    allPos = dataset.allPos
    S = []

    for user in users:
        posForUser = allPos[user]
        if len(posForUser) == 0:
            continue
        posindex = np.random.randint(0, len(posForUser))
        positem = posForUser[posindex]
        while True:
            negitem = np.random.randint(0, dataset.n_item)
            if negitem in posForUser:
                continue
            else:
                break
        S.append([user, positem, negitem])
    return torch.LongTensor(S)


def getBPR(usr_emb, pos_emb, neg_emb):
    pos_scores = torch.mul(usr_emb, pos_emb).sum(1)
    neg_scores = torch.mul(usr_emb, neg_emb).sum(1)

    return F.softplus(neg_scores - pos_scores).mean()

